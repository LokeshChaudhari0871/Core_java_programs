package com.loops;

import java.util.Scanner;

public class Square {

	public static void main(String[] args) {
	     int n,i,s;
	     Scanner sc=new Scanner (System.in);
	     
	     System.out.println("Enter the number ");
	      
	      n=sc.nextInt();
	      for(i=0;i<=n;i++)
	      {
	    	  s=n*n;
	    	  System.out.println("Square of this number is "+s);
	      }
	     
	   
		   
	   }
		   

	}


